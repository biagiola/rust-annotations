// 1. A collection type is one that can hold multiple values. Arrays, tuples, and strings are collection types.
// 2. A slice is a reference to a portion/sequence of a collection type. It's a subcategory of reference.
// 3. A string slice is a reference to a sequence of charcters from a string.
// 4. An array slice is a reference to a sequence of elements from an array.
// 5. As a reference, a slice does not take ownership of the collection